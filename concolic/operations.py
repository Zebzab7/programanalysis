class comparison:
    def _eq(a,b):
        return a==b
    def _ne(a,b):
        return a!=b
    def _ge(a,b):
        return a>=b
    def _gt(a,b):
        return a>b
    def _le(a,b):
        return a<=b
    def _lt(a,b):
        return a<b
    def _is(a,b):
        return a is b
    def _is_not(a,b):
        return a is not b

class arithmetic:
    def _add(a,b):
        return a+b
    def _sub(a,b):
        return a-b
    def _mul(a,b):
        return a*b
    def _div(a,b):
        return a/b
    def _mod(a,b):
        return a%b
    def _pow(a,b):
        return a**b 